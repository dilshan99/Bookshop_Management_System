    private void table_update()
    {
        try {
            int c;
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/stock","root", "root");
                insert = con1.prepareStatement("select * from attendance");
                ResultSet rs = insert.executeQuery();
                
                ResultSetMetaData rsd = rs.getMetaData();
                c =rsd.getColumnCount();
                
                DefaultTableModel d = (DefaultTableModel)jTable3.getModel();
                d.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1; i<=c; i++)
                    {
                        v2.add(rs.getString("Date"));
                        v2.add(rs.getString("EID"));
                        v2.add(rs.getString("Name"));
                        v2.add(rs.getString("Attendance"));
                    }
                    
                    d.addRow(v2);
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AttendanceMark.class.getName()).log(Level.SEVERE, null, ex);
            }
               
        } catch (SQLException ex) {
            Logger.getLogger(AttendanceMark.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
