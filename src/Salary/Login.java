    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //login button
        String uname = jTextField1.getText();
        String password = jPasswordField1.getText();
        String role  = jComboBox1.getSelectedItem().toString();
        
        if(uname.equals("")||password.equals("")||role.equals("Select")){
            JOptionPane.showMessageDialog(rootPane,"Some Fields Are Empty","Error", 1);
        }
        else{
            try{
                con = DBConnect.connect();
                pst = con.prepareStatement("select * from login where userName=? and password=?");
                pst.setString(1,uname);
                pst.setString(2,password);
                rs = pst.executeQuery();
                
                if(rs.next()){
                    String s1=rs.getString("role");    
                    
                    if(role.equalsIgnoreCase("Admin")&&s1.equalsIgnoreCase("admin")){
                    AdminPage ad = new AdminPage();
                    ad.setVisible(true);
                    setVisible(false);
                    }
                    
                    if(role.equalsIgnoreCase("Cashier")&&s1.equalsIgnoreCase("cashier")){
                    Cashier1 ca = new Cashier1();
                    ca.setVisible(true);
                    setVisible(false);
                    }

                }                
                else{
                    JOptionPane.showMessageDialog(rootPane,"UserName or pssword incorrect","Login Error", 1);
                }
                
            }catch(Exception e){
                System.out.println(""+e);
            }
        }
                
    }//GEN-LAST:event_jButton1ActionPerformed

