        private void DisplayTable(){
            
            String val2=txtmonth.getSelectedItem().toString();
            int val1=txtyear.getYear();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/stock","root","root");
            insert = con1.prepareStatement("select * from sales_product where Date like '%"+val1+"/"+val2+"%'");
            
            
            rs = insert.executeQuery();
            
            
            
            model = (DefaultTableModel)jTable1.getModel();
            while(rs.next()){
                
                model.addRow(new Object[]{
                    
                    rs.getString("product_id"),
                    rs.getString("Date"),
                    rs.getString("sell_price"),
                    rs.getString("qty"),
                    rs.getFloat("total")
                    
                    
                });
                
            }
            
            float sum=0;
                        
            for(int i=0; i<jTable1.getRowCount(); i++){
                sum = sum+Float.parseFloat(jTable1.getValueAt(i, 4).toString());
            }
            
            DecimalFormat df = new DecimalFormat("#.##");
            lsum = df.format(sum);
                        
            txttotal.setText(lsum);
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
        
        
    private void discount(){
        
        
        String val2=txtmonth.getSelectedItem().toString();
        int val1=txtyear.getYear();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/stock","root","root");
            insert = con1.prepareStatement("select * from sales where Date like '%"+val1+"/"+val2+"%'");
            
            
            rs = insert.executeQuery();
            
            float dis = 0;
            
            while(rs.next()){
                
                dis += rs.getFloat("SubTotal");
                
            }
            
            
            if(!(dis == 0)){
                
            DecimalFormat df = new DecimalFormat("#.##");
            
            float sum =  Float.valueOf(lsum) - dis;
            
            String fsum = df.format(sum);
            
            txtdis.setText(String.valueOf(fsum));
            
            float sub = Float.valueOf(lsum) - sum;
            
            String fsub = df.format(sub);
            
            txtsub.setText(String.valueOf(fsub));
            }
            else{
                
                txtdis.setText("0");
                txtsub.setText(lsum);
                
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SalesReport.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SalesReport.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    }
        
        
